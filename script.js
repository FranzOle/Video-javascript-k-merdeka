//Function atau Fungsi
function Fungsi (){
    console.log("Ini fungsi");
}

//Fungsi dengan nilai kembalian
function FungsiReturn(){
    return "aku belajar function";
}
//Fungsi dengan Parameter
function Luas (sisi){
    return sisi * sisi;
}
Fungsi();
console.log(FungsiReturn())
console.log(Luas(23));

